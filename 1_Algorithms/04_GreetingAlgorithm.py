# input, like print, is a Python function
# It will prompt the user to type something into the program
# using the keyboard - it will prompt the user by printing out
# out whatever String is inside the brackets - in this case, "Type your name:"
# After the user has finished typing, they must press the "Enter" key
# on the keyboard to send the data to the program.
# Whatever the user types, it will be stored in the variable called "name"
name = input("Type your name:")

#Print out a greeting to the name that the user typed!
print("Hello", name, "!")